import Foundation

/// 窗口按钮类型枚举, 包括关闭、最小化、全屏、退出全屏。
public enum LCWindowButtonType: String {
    /// 关闭按钮
    case close
    /// 最小化按钮
    case mini
    /// 进入全屏按钮
    case fullScreen
    /// 退出全屏按钮
    case exitFullScreen
}
